#include <bits/stdc++.h>
using namespace std;

class solution{
public:
    string comparison(int n, int m){
        
        if (n < m){
            
            cout << "lesser" << endl;
        }
        
        else if (n == m){
            
            cout << "equal" << endl;
        }
        
        else{
            
            cout << "greater" << endl;
        }
        
    }
};