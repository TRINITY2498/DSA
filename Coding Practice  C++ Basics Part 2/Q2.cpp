#include <bits/stdc++.h>
using namespace std;

class solution{
public:
    int add(int a, int b) {
        return a + b;
        
    }
    
    int subtract(int a, int b) {
        return a - b;
        
    }
    
    int multiply(int a, int b) {
        return a * b;
        
    }
    
    int divide(int a, int b) {
        return a / b;
        
    }
};